package com.napier.DevOps_SET09623;

/**
 * Represents a city
 */
public class City {

    /**
     * City ID
     */
    public int id;

    /**
     * City name
     */
    public String name;

    /**
     * Country name of the country of that city
     */
    public String countryName;

    /**
     * District name
     */
    public String district;

    /**
     * Population of the city
     */
    public int population;
}